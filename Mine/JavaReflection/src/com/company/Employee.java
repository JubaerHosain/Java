package com.company;

public class Employee {
    private int age;
    private long[] phoneNumbers;

    public int getAge() {
        return this.age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public long[] getPhoneNumbers() {
        return this.phoneNumbers;
    }

    public void setPhoneNumbers(long[] phoneNumbers) {
        this.phoneNumbers = phoneNumbers;
    }
}
